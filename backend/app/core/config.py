"""
App configuration - loads settings and secrets (API keys, database URL) from environment variables.
Never hardcode secrets here - they live in .env, which is not committed to git.
"""
import os
from dotenv import load_dotenv
from supabase import create_client

load_dotenv()

SUPABASE_URL = os.getenv("SUPABASE_URL")
SUPABASE_KEY = os.getenv("SUPABASE_KEY")

supabase = create_client(SUPABASE_URL, SUPABASE_KEY)
